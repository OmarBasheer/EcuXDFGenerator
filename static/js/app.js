const STEPS = ['upload', 'analyze', 'configure', 'generate', 'checksum'];

function showStep(step) {
  STEPS.forEach(s => {
    document.getElementById(`step-${s}`).classList.toggle('active', s === step);
    const link = document.getElementById(`nav-${s}`);
    if (link) link.classList.toggle('active', s === step);
  });
}

function markStepDone(step) {
  const link = document.getElementById(`nav-${step}`);
  if (link) {
    link.classList.remove('step-idle', 'step-active');
    link.classList.add('step-done');
  }
}

// ---------------------------------------------------------------------------
// Utility helpers
