function initChecksum() {
  const fileInput = el('cs-file-input');
  const dropZone  = el('cs-drop-zone');

  dropZone.addEventListener('click',     () => fileInput.click());
  dropZone.addEventListener('dragover',  e => { e.preventDefault(); dropZone.classList.add('drag-over'); });
  dropZone.addEventListener('dragleave', () => dropZone.classList.remove('drag-over'));
  dropZone.addEventListener('drop',      e => { e.preventDefault(); dropZone.classList.remove('drag-over'); handleCsFile(e.dataTransfer.files[0]); });
  fileInput.addEventListener('change',   e => handleCsFile(e.target.files[0]));

  el('btn-calc-checksum').addEventListener('click',  runChecksumCalc);
  el('btn-patch-checksum').addEventListener('click', runChecksumPatch);
  el('cs-algorithm').addEventListener('change',      updatePatchAlgoInfo);
}

function handleCsFile(file) {
  if (!file) return;
  state._csFile = file;
  el('cs-file-name').textContent = `${file.name}  (${(file.size / 1024).toFixed(1)} KB)`;
  el('cs-file-info').style.display = 'block';
  toast(`Loaded ${file.name}`, 'success');
}

async function runChecksumCalc() {
  const b64 = state.fileB64 || await getChecksumB64();
  if (!b64) { toast('Please upload a binary file first.', 'warning'); return; }

  const rStart = el('cs-region-start').value.trim() || '0x0';
  const rEnd   = el('cs-region-end').value.trim()   || '';

  spinner('spin-cs-calc', true);
  try {
    const payload = { file_data: b64, region_start: rStart };
    if (rEnd) payload.region_end = rEnd;

    const res  = await fetch('/api/checksum/calc', {
      method:  'POST',
      headers: { 'Content-Type': 'application/json' },
      body:    JSON.stringify(payload),
    });
    const data = await res.json();
    if (data.error) throw new Error(data.error);
    renderChecksumResults(data);
  } catch (err) {
    toast(`Checksum error: ${err.message}`, 'danger');
  } finally {
    spinner('spin-cs-calc', false);
  }
}

async function getChecksumB64() {
  if (state._csFile) return readFileAsB64(state._csFile);
  return null;
}

function renderChecksumResults(data) {
  el('cs-results').style.display = 'block';
  const div = el('cs-result-rows');
  div.innerHTML = '';

  const algorithms = ['sum8', 'sum16', 'sum32', 'xor8', 'negate_sum8', 'crc16', 'crc32', 'subaru', 'honda'];
  const labels = {
    sum8: 'Sum8', sum16: 'Sum16', sum32: 'Sum32',
    xor8: 'XOR8', negate_sum8: 'Negate-Sum8',
    crc16: 'CRC16 (CCITT)', crc32: 'CRC32 (ISO)',
    subaru: 'Subaru / Denso', honda: 'Honda OBD',
  };

  algorithms.forEach(algo => {
    const r = data[algo];
    if (!r) return;
    div.insertAdjacentHTML('beforeend', `
      <div class="cs-row">
        <span class="cs-label">${labels[algo] || algo}</span>
        <span class="cs-val">${r.value}</span>
      </div>
    `);
  });

  // Region info
  div.insertAdjacentHTML('afterbegin', `
    <div class="cs-row fw-semibold border-bottom mb-1" style="color:#212529">
      <span>Region</span>
      <span class="mono">${data.region.start_hex} → ${data.region.end_hex}
        (${(data.region.size / 1024).toFixed(1)} KB)</span>
    </div>
  `);

  // Copy subaru description if present
  if (data.subaru?.description) {
    div.insertAdjacentHTML('beforeend', `
      <p class="small text-muted mt-1 mb-0">${data.subaru.description}</p>
    `);
  }
}

async function runChecksumPatch() {
  const b64 = state.fileB64 || await getChecksumB64();
  if (!b64) { toast('Please upload a binary file first.', 'warning'); return; }

  const algo   = el('cs-algorithm').value;
  const rStart = el('cs-region-start').value.trim()  || '0x0';
  const rEnd   = el('cs-region-end').value.trim()    || '';
  const stor   = el('cs-storage-addr').value.trim()  || '0x0';

  spinner('spin-cs-patch', true);
  try {
    const payload = {
      file_data:       b64,
      algorithm:       algo,
      region_start:    rStart,
      storage_address: stor,
    };
    if (rEnd) payload.region_end = rEnd;

    const res = await fetch('/api/checksum/patch', {
      method:  'POST',
      headers: { 'Content-Type': 'application/json' },
      body:    JSON.stringify(payload),
    });

    if (!res.ok) {
      const err = await res.json();
      throw new Error(err.error);
    }
    const blob = await res.blob();
    downloadBlob(blob, 'patched.bin');
    toast('Patched binary downloaded!', 'success');
  } catch (err) {
    toast(`Patch failed: ${err.message}`, 'danger');
  } finally {
    spinner('spin-cs-patch', false);
  }
}

function updatePatchAlgoInfo() {
  const algo  = el('cs-algorithm').value;
  const sizes = { subaru: '4 bytes LE (Denso magic)', honda: '1 byte (2\'s complement)', crc32: '4 bytes LE', sum32: '4 bytes LE', sum16: '2 bytes LE' };
  el('cs-algo-info').textContent = sizes[algo] || '';
}

