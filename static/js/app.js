function renderAnalysis(a) {
  // File info
  el('a-size').textContent = `${a.file_info.size_kb} KB (${a.file_info.size_hex})`;
  el('a-md5').textContent  = a.file_info.md5;
  el('a-sha1').textContent = a.file_info.sha1;
  el('a-fill').textContent = a.file_info.fill_byte ?? '—';

  // ECU detection
  el('a-ecu-primary').textContent = a.suggested_ecu.primary;
  const sugDiv = el('a-ecu-suggestions');
  sugDiv.innerHTML = '';
  a.suggested_ecu.suggestions.forEach(s => {
    sugDiv.insertAdjacentHTML('beforeend', `<span class="badge bg-info text-dark me-1 mb-1">${s}</span>`);
  });

  // Matching profiles → auto-select first one
  if (a.suggested_ecu.profiles.length > 0) {
    const sel = el('cfg-profile');
    if (sel) {
      sel.value = a.suggested_ecu.profiles[0];
      applyProfile(a.suggested_ecu.profiles[0]);
    }
  }

  // Regions
  const regDiv = el('a-regions');
  regDiv.innerHTML = '';
  a.regions.forEach(r => {
    regDiv.insertAdjacentHTML('beforeend',
      `<span class="badge region-${r.type} me-1 mb-1" title="${r.start_hex}–${r.end_hex}">
        ${r.start_hex} (${(r.size / 1024).toFixed(0)} KB, H=${r.entropy})
      </span>`
    );
  });

  // Signatures
  const sigDiv = el('a-signatures');
  sigDiv.innerHTML = '';
  a.signatures.forEach(s => {
    sigDiv.insertAdjacentHTML('beforeend',
      `<div class="suggest-card mb-1">
        <span class="addr">${s.address_hex}</span>
        <span class="ms-2 text-muted">${escHtml(s.value)}</span>
       </div>`
    );
  });

  // Checksum hints
  const csDiv = el('a-checksum-hints');
  csDiv.innerHTML = '';
  a.checksum_hints.forEach(h => {
    csDiv.insertAdjacentHTML('beforeend',
      `<div class="suggest-card mb-1">
        <span class="addr">${h.address_hex}</span>
        <span class="ms-2">${escHtml(h.description)}</span>
       </div>`
    );
  });

  // Potential axes
  renderAxesSuggestions(a.potential_axes);

  // Potential tables
  renderTableSuggestions(a.potential_tables);
}

function renderAxesSuggestions(axes) {
  const div = el('a-axes');
  div.innerHTML = '';
  if (!axes.length) { div.textContent = 'No axis patterns detected.'; return; }
  axes.slice(0, 30).forEach(ax => {
    const preview = ax.values.slice(0, 8).join(', ') + (ax.values.length > 8 ? ' …' : '');
    const card = document.createElement('div');
    card.className = 'suggest-card mb-1';
    card.title = 'Click to add as X-axis breakpoints in Configure step';
    card.innerHTML = `
      <span class="addr">${ax.address_hex}</span>
      <span class="ms-2 fw-bold">${ax.count}×${ax.element_size}-bit</span>
      <span class="ms-2 text-muted">${ax.type}</span>
      <span class="ms-2 mono text-secondary">[${preview}]</span>
      <button class="btn btn-xs btn-outline-primary ms-auto" title="Add 1D table at this axis">+ 1D</button>
    `;
    card.querySelector('button').addEventListener('click', e => {
      e.stopPropagation();
      prefillFrom1DAxis(ax);
    });
    div.appendChild(card);
  });
}

function renderTableSuggestions(tables) {
  const div = el('a-tables');
  div.innerHTML = '';
  if (!tables.length) { div.textContent = 'No table patterns detected.'; return; }
  tables.forEach(t => {
    const card = document.createElement('div');
    card.className = 'suggest-card mb-1';
    card.title = 'Click to add as 2D table';
    card.innerHTML = `
      <span class="addr">${t.address_hex}</span>
      <span class="ms-2 fw-bold">${t.label}</span>
      <span class="ms-2 text-muted">${t.size} bytes</span>
      <span class="ms-2 mono text-secondary">[${t.sample.slice(0,8).join(', ')}…]</span>
      <button class="btn btn-xs btn-outline-success ms-auto">+ 2D</button>
    `;
    card.querySelector('button').addEventListener('click', e => {
      e.stopPropagation();
      prefillFrom2DTable(t);
    });
    div.appendChild(card);
  });
}

function prefillFrom1DAxis(ax) {
  el('t-type').value     = '1d';
  el('t-title').value    = `Axis at ${ax.address_hex}`;
  el('t-x-addr').value   = ax.address_hex;
  el('t-x-size').value   = ax.element_size;
  el('t-count').value    = ax.count;
  updateTypeFields();
  showStep('configure');
  bootstrap.Modal.getOrCreateInstance(el('tableModal')).show();
}

function prefillFrom2DTable(t) {
  el('t-type').value   = '2d';
  el('t-title').value  = `Table at ${t.address_hex}`;
  el('t-addr').value   = t.address_hex;
  el('t-rows').value   = t.rows;
  el('t-cols').value   = t.cols;
  updateTypeFields();
  showStep('configure');
  bootstrap.Modal.getOrCreateInstance(el('tableModal')).show();
}

