function initUpload() {
  const zone  = el('drop-zone');
  const input = el('file-input');

  zone.addEventListener('click',      () => input.click());
  zone.addEventListener('dragover',   e => { e.preventDefault(); zone.classList.add('drag-over'); });
  zone.addEventListener('dragleave',  () => zone.classList.remove('drag-over'));
  zone.addEventListener('drop',       e => { e.preventDefault(); zone.classList.remove('drag-over'); handleFile(e.dataTransfer.files[0]); });
  input.addEventListener('change',    e => handleFile(e.target.files[0]));

  el('btn-analyze').addEventListener('click', runAnalysis);
}

function handleFile(file) {
  if (!file) return;
  if (!file.name.toLowerCase().endsWith('.bin')) {
    toast('Please select a .bin ECU file.', 'warning');
    return;
  }
  state.fileName = file.name;
  state.fileSize = file.size;
  el('file-name').textContent    = file.name;
  el('file-size').textContent    = `${(file.size / 1024).toFixed(1)} KB (${hexFmt(file.size)})`;
  el('file-preview').style.display = 'block';
  el('btn-analyze').disabled = false;

  // store the selected File object for upload
  state._file = file;
}

async function runAnalysis() {
  if (!state._file) return;
  spinner('spin-analyze', true);
  el('btn-analyze').disabled = true;

  const form = new FormData();
  form.append('file', state._file);

  try {
    const res  = await fetch('/api/analyze', { method: 'POST', body: form });
    const data = await res.json();
    if (data.error) throw new Error(data.error);

    state.analysis = data;
    state.fileB64  = data.file_b64;
    state.xdfConfig.file_size = data.file_info.size;

    renderAnalysis(data);
    markStepDone('upload');
    showStep('analyze');
  } catch (err) {
    toast(`Analysis failed: ${err.message}`, 'danger');
  } finally {
    spinner('spin-analyze', false);
    el('btn-analyze').disabled = false;
  }
}

