function applyProfile(profileId) {
  const p = state.profiles.find(x => x.id === profileId);
  if (!p) return;
  state.xdfConfig.categories = p.default_categories || state.xdfConfig.categories;
  el('cfg-title').value       ||= `${p.name} XDF`;
  el('cfg-categories').value  = state.xdfConfig.categories.join(', ');
  // Pre-fill checksum defaults
  if (p.checksum) {
    el('cs-algorithm').value   = p.checksum.algorithm;
    el('cs-region-start').value = p.checksum.region_start;
    el('cs-region-end').value   = p.checksum.region_end;
    el('cs-storage-addr').value = p.checksum.storage_address;
  }
  toast(`Profile "${p.name}" applied`, 'success');
}

function openAddTableModal() {
  // Reset form
  el('table-form').reset();
  el('t-size').value = '8';
  updateTypeFields();
  el('edit-index').value = '-1';
  bootstrap.Modal.getOrCreateInstance(el('tableModal')).show();
}

function updateTypeFields() {
  const type = el('t-type').value;
  el('row-addr').style.display     = (type !== '1d') ? '' : 'none';
  el('row-x-addr').style.display   = (type === '1d' || type === '2d') ? '' : 'none';
  el('row-y-addr').style.display   = (type === '2d') ? '' : 'none';
  el('row-count').style.display    = (type === '1d') ? '' : 'none';
  el('row-dims').style.display     = (type === '2d') ? '' : 'none';
  el('row-x-size').style.display   = (type === '1d' || type === '2d') ? '' : 'none';
  el('row-mask').style.display     = (type === 'flag') ? '' : 'none';
  el('row-x-math').style.display   = (type === '1d' || type === '2d') ? '' : 'none';
  el('row-y-math').style.display   = (type === '2d') ? '' : 'none';
  el('row-units').style.display    = (type !== 'flag') ? '' : 'none';
}

function saveTable() {
  const type      = el('t-type').value;
  const title     = el('t-title').value.trim() || 'Unnamed';
  const addr      = el('t-addr').value.trim()  || '0x0';
  const xAddr     = el('t-x-addr').value.trim() || '0x0';
  const yAddr     = el('t-y-addr').value.trim() || '0x0';
  const count     = parseInt(el('t-count').value) || 16;
  const rows      = parseInt(el('t-rows').value)  || 16;
  const cols      = parseInt(el('t-cols').value)  || 16;
  const elemSize  = parseInt(el('t-size').value)  || 8;
  const xElemSize = parseInt(el('t-x-size').value) || elemSize;
  const math      = el('t-math').value.trim()  || 'X';
  const xMath     = el('t-x-math').value.trim() || 'X';
  const yMath     = el('t-y-math').value.trim() || 'X';
  const units     = el('t-units').value.trim();
  const mask      = el('t-mask').value.trim()  || '0x1';
  const desc      = el('t-desc').value.trim();
  const signed_   = el('t-signed').checked;
  const lsbFirst  = el('t-lsb').checked;

  const tbl = {
    type, title, desc, math, units, signed: signed_, lsb_first: lsbFirst,
    address: addr, element_size: elemSize,
  };

  if (type === '1d') {
    Object.assign(tbl, { x_address: xAddr, x_element_size: xElemSize, x_math: xMath, count });
  } else if (type === '2d') {
    Object.assign(tbl, { x_address: xAddr, y_address: yAddr, x_element_size: xElemSize, x_math: xMath, y_math: yMath, rows, cols });
  } else if (type === 'flag') {
    tbl.mask = mask;
  }

  const editIdx = parseInt(el('edit-index').value);
  if (editIdx >= 0) {
    state.xdfConfig.tables[editIdx] = tbl;
  } else {
    state.xdfConfig.tables.push(tbl);
  }

  bootstrap.Modal.getInstance(el('tableModal')).hide();
  renderTableList();
}

function renderTableList() {
  const list = el('table-list');
  list.innerHTML = '';
  if (!state.xdfConfig.tables.length) {
    list.innerHTML = '<p class="text-muted small">No tables defined yet. Click "Add" or use suggestions from the Analyze step.</p>';
    return;
  }
  state.xdfConfig.tables.forEach((t, i) => {
    const item = document.createElement('div');
    item.className = 'table-item';
    item.innerHTML = `
      <span class="ti-type">${t.type}</span>
      <span class="ti-title">${escHtml(t.title)}</span>
      <span class="ti-addr">${t.address || t.z_address || ''}</span>
      <div class="ms-auto d-flex gap-1">
        <button class="btn btn-xs btn-outline-secondary" data-action="edit" data-i="${i}">Edit</button>
        <button class="btn btn-xs btn-outline-danger"   data-action="del"  data-i="${i}">✕</button>
      </div>
    `;
    item.querySelectorAll('button').forEach(btn => btn.addEventListener('click', e => {
      const action = e.target.dataset.action;
      const idx    = parseInt(e.target.dataset.i);
      if (action === 'del') {
        state.xdfConfig.tables.splice(idx, 1);
        renderTableList();
      } else if (action === 'edit') {
        editTable(idx);
      }
    }));
    list.appendChild(item);
  });
}

function editTable(i) {
  const t = state.xdfConfig.tables[i];
  el('t-type').value   = t.type;
  el('t-title').value  = t.title;
  el('t-desc').value   = t.desc || '';
  el('t-addr').value   = t.address || '';
  el('t-size').value   = t.element_size || 8;
  el('t-math').value   = t.math || 'X';
  el('t-units').value  = t.units || '';
  el('t-signed').checked   = !!t.signed;
  el('t-lsb').checked      = !!t.lsb_first;
  if (t.type === '1d') {
    el('t-x-addr').value = t.x_address || '';
    el('t-x-size').value = t.x_element_size || 8;
    el('t-x-math').value = t.x_math || 'X';
    el('t-count').value  = t.count || 16;
  } else if (t.type === '2d') {
    el('t-x-addr').value = t.x_address || '';
    el('t-y-addr').value = t.y_address || '';
    el('t-x-size').value = t.x_element_size || 8;
    el('t-x-math').value = t.x_math || 'X';
    el('t-y-math').value = t.y_math || 'X';
    el('t-rows').value   = t.rows || 16;
    el('t-cols').value   = t.cols || 16;
  } else if (t.type === 'flag') {
    el('t-mask').value = t.mask || '0x1';
  }
  updateTypeFields();
  el('edit-index').value = i;
  bootstrap.Modal.getOrCreateInstance(el('tableModal')).show();
}

function buildXdfConfig() {
  const cats = el('cfg-categories').value
    .split(',')
    .map(s => s.trim())
    .filter(Boolean);

  state.xdfConfig.title       = el('cfg-title').value.trim()       || 'My ECU XDF';
  state.xdfConfig.author      = el('cfg-author').value.trim();
  state.xdfConfig.description = el('cfg-description').value.trim() || '';
  state.xdfConfig.categories  = cats;
  state.xdfConfig.lsb_first   = el('cfg-lsb').checked;
  state.xdfConfig.default_size = parseInt(el('cfg-default-size').value) || 8;
}

