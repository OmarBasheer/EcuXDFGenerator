async function runPreview() {
  spinner('spin-preview', true);
  try {
    const res  = await fetch('/api/preview', {
      method:  'POST',
      headers: { 'Content-Type': 'application/json' },
      body:    JSON.stringify(state.xdfConfig),
    });
    const data = await res.json();
    if (data.error) throw new Error(data.error);
    el('xml-preview').textContent = data.xml;
  } catch (err) {
    el('xml-preview').textContent = `Error: ${err.message}`;
  } finally {
    spinner('spin-preview', false);
  }
}

async function downloadXdf() {
  buildXdfConfig();
  spinner('spin-download', true);
  try {
    const res = await fetch('/api/generate', {
      method:  'POST',
      headers: { 'Content-Type': 'application/json' },
      body:    JSON.stringify(state.xdfConfig),
    });
    if (!res.ok) {
      const err = await res.json();
      throw new Error(err.error);
    }
    const blob     = await res.blob();
    const filename = (state.xdfConfig.title || 'generated').replace(/\s+/g, '_') + '.xdf';
    downloadBlob(blob, filename);
    markStepDone('generate');
    toast('XDF downloaded successfully!', 'success');
  } catch (err) {
    toast(`Download failed: ${err.message}`, 'danger');
  } finally {
    spinner('spin-download', false);
  }
}

