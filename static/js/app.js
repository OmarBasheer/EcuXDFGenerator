function el(id) { return document.getElementById(id); }

function toast(msg, type = 'info') {
  const container = el('toast-container');
  const div = document.createElement('div');
  div.className = `alert alert-${type} alert-dismissible fade show shadow-sm mb-2`;
  div.style.cssText = 'min-width:280px;font-size:.88rem';
  div.innerHTML = `${msg}<button type="button" class="btn-close" data-bs-dismiss="alert"></button>`;
  container.appendChild(div);
  setTimeout(() => div.remove(), 5000);
}

function spinner(id, show) {
  const el_ = el(id);
  if (el_) el_.style.display = show ? 'inline-block' : 'none';
}

function hexFmt(n) { return `0x${n.toString(16).toUpperCase().padStart(2, '0')}`; }

function readFileAsB64(file) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload  = e => resolve(e.target.result.split(',')[1]);
    reader.onerror = reject;
    reader.readAsDataURL(file);
  });
}

